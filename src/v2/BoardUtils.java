    public static int countEmptyCells(int[][] board){
        int counter = 0;
        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board.length; j++) {
                if(board[i][j] == 0)
                    counter++;
            }
        }
        return counter;
    }

    public static int countEmptyCells(Cell[][] board){
        int counter = 0;
        for (int i = 0; i < board.length; i++) {
            for (int j = 0; j < board.length; j++) {
                if(board[i][j].isEmpty())
                    counter++;
            }
        }
        return counter;
    }

